function replaceRequired(source, name, original, replacement) {
  if (source.includes(replacement)) return source;
  const patched = source.replace(original, replacement);
  if (patched === source) {
    throw new Error(`Could not locate the expected ${name} block.`);
  }
  return patched;
}

function patchManualServerSelection(source) {
  if (
    source.includes('const manualSourceSelectionRef = useRef(false);') &&
    source.includes('Selected server seek failed; retrying the same server')
  ) {
    return source;
  }

  let patched = source;

  patched = replaceRequired(
    patched,
    'source identity refs',
    '  const failedSourcesRef = useRef(new Set<number>());',
    [
      '  const failedSourcesRef = useRef(new Set<string>());',
      '  const selectedSourceUrlRef = useRef(videoLink.url);',
      '  const manualSourceSelectionRef = useRef(false);',
      '  const manualSeekRecoveryRef = useRef(new Map<string, number>());',
    ].join('\n'),
  );

  patched = replaceRequired(
    patched,
    'stable source reconciliation',
    [
      '  useEffect(() => {',
      '    if (activeSourceIndex >= availableSources.length) setActiveSourceIndex(0);',
      '  }, [activeSourceIndex, availableSources.length]);',
    ].join('\n'),
    [
      '  useEffect(() => {',
      '    if (availableSources.length === 0) return;',
      '',
      '    const selectedUrl = selectedSourceUrlRef.current;',
      '    const selectedIndex = availableSources.findIndex(',
      '      (source) => source.url === selectedUrl,',
      '    );',
      '    if (selectedIndex >= 0) {',
      '      if (selectedIndex !== activeSourceIndex) setActiveSourceIndex(selectedIndex);',
      '      return;',
      '    }',
      '',
      '    // Preserve the exact selected URL when source metadata refreshes or',
      '    // the list order changes. A numeric index alone can point at another',
      '    // server after the source array is rebuilt.',
      '    selectedSourceUrlRef.current = availableSources[0].url;',
      '    manualSourceSelectionRef.current = false;',
      '    manualSeekRecoveryRef.current.clear();',
      '    setActiveSourceIndex(0);',
      '  }, [activeSourceIndex, availableSources]);',
      '',
      '  useEffect(() => {',
      '    selectedSourceUrlRef.current = videoLink.url;',
      '    manualSourceSelectionRef.current = false;',
      '    manualSeekRecoveryRef.current.clear();',
      '    failedSourcesRef.current.clear();',
      '    setActiveSourceIndex(0);',
      '  }, [contentId, videoLink.url]);',
    ].join('\n'),
  );

  patched = replaceRequired(
    patched,
    'manual source switch',
    [
      '  const switchSource = useCallback(',
      '    (index: number) => {',
      '      if (index < 0 || index >= availableSources.length) return;',
      '      persistPlaybackPosition(true);',
      '      setGooglePlaybackOverride(null);',
      '      const video = videoRef.current;',
      '      if (video) {',
      '        resumeTimeRef.current = video.currentTime;',
      '        shouldResumePlayingRef.current = !video.paused;',
      '      }',
      '      failedSourcesRef.current.delete(index);',
      "      setErrorMessage('');",
      "      setStatus('preparing');",
      '      setSettingsOpen(false);',
      '      if (index === activeSourceIndex) setReloadToken((value) => value + 1);',
      '      else setActiveSourceIndex(index);',
      '    },',
      '    [activeSourceIndex, availableSources.length, persistPlaybackPosition],',
      '  );',
    ].join('\n'),
    [
      '  const switchSource = useCallback(',
      '    (index: number) => {',
      '      if (index < 0 || index >= availableSources.length) return;',
      '      const selectedSource = availableSources[index];',
      '      persistPlaybackPosition(true);',
      '      setGooglePlaybackOverride(null);',
      '      const video = videoRef.current;',
      '      if (video) {',
      '        resumeTimeRef.current = video.currentTime;',
      '        shouldResumePlayingRef.current = !video.paused;',
      '      }',
      '',
      '      // A viewer-selected server remains pinned until another server is',
      '      // selected explicitly. Seeking must not silently select index zero.',
      '      selectedSourceUrlRef.current = selectedSource.url;',
      '      manualSourceSelectionRef.current = true;',
      '      manualSeekRecoveryRef.current.delete(selectedSource.url);',
      '      failedSourcesRef.current.delete(selectedSource.url);',
      "      setErrorMessage('');",
      "      setStatus('preparing');",
      '      setSettingsOpen(false);',
      '      if (index === activeSourceIndex) setReloadToken((value) => value + 1);',
      '      else setActiveSourceIndex(index);',
      '    },',
      '    [activeSourceIndex, availableSources, persistPlaybackPosition],',
      '  );',
    ].join('\n'),
  );

  patched = replaceRequired(
    patched,
    'source failure handling',
    [
      '  const failCurrentSource = useCallback(',
      '    (message: string) => {',
      '      pendingSeekRef.current = null;',
      '      setSeekPreview(null);',
      '      persistPlaybackPosition(true);',
      '      failedSourcesRef.current.add(activeSourceIndex);',
      '      const nextIndex = availableSourcesRef.current.findIndex(',
      '        (_, index) => !failedSourcesRef.current.has(index),',
      '      );',
      '',
      '      if (nextIndex >= 0) {',
      '        const video = videoRef.current;',
      '        if (video) {',
      '          resumeTimeRef.current = video.currentTime;',
      '          shouldResumePlayingRef.current = !video.paused || autoPlay;',
      '        }',
      "        setStatus('preparing');",
      '        setGooglePlaybackOverride(null);',
      '        setActiveSourceIndex(nextIndex);',
      '        return;',
      '      }',
      '',
      "      setStatus('error');",
      '      setErrorMessage(message);',
      '      setIsPlaying(false);',
      '      onError?.(message);',
      '    },',
      '    [activeSourceIndex, autoPlay, onError, persistPlaybackPosition],',
      '  );',
    ].join('\n'),
    [
      '  const failCurrentSource = useCallback(',
      '    (message: string) => {',
      '      const video = videoRef.current;',
      '      const activeUrl =',
      '        activePlaybackSource?.url ||',
      '        availableSourcesRef.current[activeSourceIndex]?.url ||',
      "        '';",
      '      const requestedPosition =',
      '        pendingSeekRef.current ??',
      '        (video && Number.isFinite(video.currentTime) ? video.currentTime : 0);',
      '      const shouldContinuePlaying = video',
      '        ? !video.paused || autoPlay',
      '        : shouldResumePlayingRef.current || autoPlay;',
      '',
      '      pendingSeekRef.current = null;',
      '      setSeekPreview(null);',
      '      persistPlaybackPosition(true);',
      '',
      '      const manuallyPinned =',
      '        Boolean(activeUrl) &&',
      '        manualSourceSelectionRef.current &&',
      '        selectedSourceUrlRef.current === activeUrl;',
      '',
      '      if (manuallyPinned) {',
      '        const attempts = manualSeekRecoveryRef.current.get(activeUrl) ?? 0;',
      '        if (requestedPosition > 0 && attempts < 2) {',
      '          manualSeekRecoveryRef.current.set(activeUrl, attempts + 1);',
      '          resumeTimeRef.current = requestedPosition;',
      '          shouldResumePlayingRef.current = shouldContinuePlaying;',
      "          setErrorMessage('');",
      "          setStatus('preparing');",
      '          setGooglePlaybackOverride(null);',
      '          setReloadToken((value) => value + 1);',
      '          console.warn(',
      '            `Selected server seek failed; retrying the same server (${attempts + 1}/2):`,',
      '            message,',
      '          );',
      '          return;',
      '        }',
      '',
      '        // Keep the selected card active. An explicit error is preferable',
      '        // to silently replacing the server selected by the viewer.',
      "        setStatus('error');",
      '        setErrorMessage(message);',
      '        setIsPlaying(false);',
      '        onError?.(message);',
      '        return;',
      '      }',
      '',
      '      if (activeUrl) failedSourcesRef.current.add(activeUrl);',
      '      const nextIndex = availableSourcesRef.current.findIndex(',
      '        (source) => !failedSourcesRef.current.has(source.url),',
      '      );',
      '',
      '      if (nextIndex >= 0) {',
      '        const nextSource = availableSourcesRef.current[nextIndex];',
      '        if (video) {',
      '          resumeTimeRef.current = video.currentTime;',
      '          shouldResumePlayingRef.current = !video.paused || autoPlay;',
      '        }',
      '        selectedSourceUrlRef.current = nextSource.url;',
      '        manualSourceSelectionRef.current = false;',
      "        setStatus('preparing');",
      '        setGooglePlaybackOverride(null);',
      '        setActiveSourceIndex(nextIndex);',
      '        return;',
      '      }',
      '',
      "      setStatus('error');",
      '      setErrorMessage(message);',
      '      setIsPlaying(false);',
      '      onError?.(message);',
      '    },',
      '    [',
      '      activePlaybackSource?.url,',
      '      activeSourceIndex,',
      '      autoPlay,',
      '      onError,',
      '      persistPlaybackPosition,',
      '    ],',
      '  );',
    ].join('\n'),
  );

  return patched;
}

module.exports = { patchManualServerSelection };
